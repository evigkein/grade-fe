export const secondInMs = 1000;
export const minuteInMs = secondInMs * 60; // 1000 * 60
export const hourInMs = minuteInMs * 60; // 1000 * 60 * 60
export const dayInMs = hourInMs * 24; // 1000 * 60 * 60 * 24
export const weekInMs = dayInMs * 7; // 1000 * 60 * 60 * 24 * 7
export const monthInMs = dayInMs * 30; // 1000 * 60 * 60 * 24 * 30
